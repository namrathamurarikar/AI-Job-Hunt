/** Checkbox marks in data/pipeline.md (single character inside [...]). */
function markToPipelineStatus(mark) {
  const m = String(mark || '').trim();
  if (!m) return 'pending';
  switch (m) {
    case 'x':
      return 'evaluated';
    case '!':
      return 'interviewing';
    case 'a':
      return 'applied';
    case 'r':
      return 'rejected';
    case 'o':
      return 'offer';
    default:
      return 'pending';
  }
}

/** Inverse: dashboard status → one character for markdown checkbox line. */
function pipelineStatusToMark(nextStatus) {
  const s = String(nextStatus || 'pending').toLowerCase();
  if (s === 'pending') return ' ';
  if (s === 'evaluated') return 'x';
  if (s === 'interviewing') return '!';
  if (s === 'applied') return 'a';
  if (s === 'rejected') return 'r';
  if (s === 'offer') return 'o';
  return 'x';
}

/** applications.md status column uses canonical labels (see templates/states.yml). */
function dashboardStatusToCanonical(dashboardStatus) {
  const s = String(dashboardStatus || '').toLowerCase();
  if (s === 'pending') return null;
  const map = {
    evaluated: 'Evaluated',
    applied: 'Applied',
    interviewing: 'Interview',
    rejected: 'Rejected',
    offer: 'Offer',
  };
  return map[s] ?? null;
}

function parsePipeline() {
  const text = safeRead(PATHS.pipeline, '');
  const rows = [];
  const lineRows = text.split(/\r?\n/);

  let id = 1;
  for (const line of lineRows) {
    const m = line.match(/^\s*-\s*\[([^\]]*)\]\s+(https?:\/\/\S+)\s*\|\s*([^|]+)\s*\|\s*(.+)\s*$/i);
    if (!m) continue;
    const status = markToPipelineStatus(m[1]);
    rows.push({
      id: id++,
      source: 'checkbox',
      line,
      status,
      url: m[2].trim(),
      company: m[3].trim(),
      title: m[4].trim(),
    });
  }

  if (rows.length > 0) return rows;

  // Table fallback
  for (const line of lineRows) {
    if (!line.trim().startsWith('|')) continue;
    if (/^\|\s*-+/.test(line) || /title/i.test(line)) continue;
    const cells = line.split('|').map((x) => x.trim()).filter(Boolean);
    if (cells.length < 4) continue;
    rows.push({
      id: id++,
      source: 'table',
      line,
      title: cells[0],
      company: cells[1],
      url: cells[2],
      status: (cells[3] || 'pending').toLowerCase(),
    });
  }
  return rows;
}

function parseApplications() {
  const text = safeRead(PATHS.applications, '');
  const rows = [];
  let id = 1;
  for (const line of text.split(/\r?\n/)) {
    if (!line.startsWith('|')) continue;
    if (line.includes('|---') || /\|\s*#\s*\|/.test(line)) continue;
    const c = line.split('|').map((x) => x.trim()).slice(1, -1);
    if (c.length < 9) continue;
    rows.push({
      id: id++,
      num: c[0],
      date: c[1],
      company: c[2],
      role: c[3],
      score: c[4],
      status: String(c[5] || 'Pending').toLowerCase(),
      pdf: c[6],
      report: c[7],
      notes: c[8] || '',
      line,
    });
  }
  return rows;
}

function parseScanHistory() {
  const text = safeRead(PATHS.scanHistory, '');
  const lines = text.split(/\r?\n/).filter(Boolean);
  const rows = [];
  for (const line of lines.slice(1)) {
    const [url, first_seen, portal, title, company, status] = line.split('\t');
    if (!url) continue;
    rows.push({ url, first_seen, portal, title, company, status });
  }
  return rows;
}

/**
 * @param {string} path
 * @param {string[]} exts
 * @param {string} [base]
 * @param {{ skipDirs?: Set<string> }} [options]
 */
function listFiles(path, exts, base = path, options = {}) {
  if (!existsSync(path)) return [];
  const skipDirs = options.skipDirs || new Set();
  const out = [];
  for (const f of readdirSync(path)) {
    const p = join(path, f);
    const s = statSync(p);
    if (s.isDirectory()) {
      if (skipDirs.has(f)) continue;
      out.push(...listFiles(p, exts, base, options));
      continue;
    }
    if (!exts.includes(extname(f).toLowerCase())) continue;
    const relPath = p.slice(base.length + 1).replaceAll('\\', '/');
    out.push({
      name: f,
      relPath,
      path: p,
      size: s.size,
      mtime: s.mtime.toISOString(),
    });
  }
  return out;
}

/** Align tracker wording with dashboard-ui.html `<select>` (canonical Interview → interviewing). */
function normalizeDashboardStatus(s) {
  const v = String(s || 'pending').toLowerCase();
  if (v === 'interview') return 'interviewing';
  return v;
}

/**
 * Pipeline inbox line is the source of truth for workflow state once it is not `[ ]`.
 * If the line is still pending, use applications.md (evaluated via /career-ops) for score/status.
 */
function mergedPipelineApplicationStatus(app, job) {
  const j = job?.status || 'pending';
  if (j !== 'pending') return j;
  return app?.status || 'pending';
}

function extractScoreFromReportText(md) {
  const text = String(md || '');
  const m =
    text.match(/\*\*Score:\*\*\s*([0-5](?:\.\d+)?)\s*\/\s*5/i) ||
    text.match(/\*\*Overall Score:\*\*\s*([0-5](?:\.\d+)?)\s*\/\s*5/i) ||
    text.match(/\bScore:\s*([0-5](?:\.\d+)?)\s*\/\s*5/i);
  if (!m) return null;
  return `${m[1]}/5`;
}

function reportScoreForJob(job, reportFiles) {
  if (!Array.isArray(reportFiles) || reportFiles.length === 0) return null;
  const companySlug = slug(job.company);
  const roleSlug = slug(job.title);
  const hit =
    reportFiles.find((r) => slug(r.name).includes(companySlug) && slug(r.name).includes(roleSlug)) ||
    reportFiles.find((r) => slug(r.name).includes(companySlug)) ||
    null;
  if (!hit) return null;
  const md = safeRead(hit.path, '');
  return extractScoreFromReportText(md);
}

function enrichJobs(jobs, options = {}) {
  const appRows = parseApplications();
  const histByUrl = new Map(parseScanHistory().map((h) => [h.url, h]));
  const reportFiles = listFiles(PATHS.reports, ['.md']);
  const autoScore = options.autoScore !== false;
  return jobs.map((job) => {
    const app = appRows.find((a) => slug(a.company) === slug(job.company) && slug(a.role) === slug(job.title));
    const hist = histByUrl.get(job.url);
    const raw = mergedPipelineApplicationStatus(app, job);
    const score = app?.score || reportScoreForJob(job, reportFiles) || (autoScore ? estimateJobScore(job.title) : '—/5');
    return {
      ...job,
      status: normalizeDashboardStatus(raw),
      score,
      dateAdded: hist?.first_seen || '—',
      reportHint: app?.report || '',
      hasTrackerRow: Boolean(app),
    };
  });
}

function stats() {
  const jobs = enrichJobs(parsePipeline());
  const resumes = listFiles(PATHS.output, ['.docx', '.pdf', '.html'], PATHS.output, OUTPUT_RESUME_LIST_OPTS);
  const reports = listFiles(PATHS.reports, ['.md']);
  const applied = jobs.filter((j) => /applied/.test(j.status)).length;
  const inProgress = jobs.filter((j) => /(interview|screen|progress)/.test(j.status)).length;
  return {
    pipelineTotal: jobs.length,
    applied,
    inProgress,
    resumesGenerated: resumes.length,
    evaluationsDone: reports.length,
    running: Boolean(runningScript),
    runningScript,
  };
}

function writePipelineStatus(id, nextStatus) {
  const text = safeRead(PATHS.pipeline, '');
  const lines = text.split(/\r?\n/);
  const jobs = parsePipeline();
  const job = jobs.find((j) => j.id === Number(id));
  if (!job) return false;

  if (job.source === 'checkbox') {
    const mark = pipelineStatusToMark(nextStatus);
    const bracket = mark === ' ' ? '[ ]' : `[${mark}]`;
    const nextLine = job.line.replace(/\[[^\]]*\]/, bracket);
    const idx = lines.findIndex((ln) => ln === job.line);
    if (idx >= 0) lines[idx] = nextLine;
  } else {
    const idx = lines.findIndex((ln) => ln === job.line);
    if (idx >= 0) {
      const cells = job.line.split('|').map((x) => x.trim()).filter(Boolean);
      if (cells.length >= 4) {
        lines[idx] = `| ${cells[0]} | ${cells[1]} | ${cells[2]} | ${nextStatus} |`;
      }
    }
  }
  writeFileSync(PATHS.pipeline, lines.join('\n'), 'utf-8');
  return true;
}

function writeApplicationStatus(id, nextStatus) {
  if (!existsSync(PATHS.applications)) return false;
  const text = safeRead(PATHS.applications, '');
  const lines = text.split(/\r?\n/);
  const apps = parseApplications();
  const appRow = apps.find((a) => a.id === Number(id));
  if (!appRow) return false;
  const idx = lines.findIndex((ln) => ln === appRow.line);
  if (idx < 0) return false;
  const c = appRow.line.split('|').map((x) => x.trim()).slice(1, -1);
  if (c.length < 9) return false;
  c[5] = nextStatus;
  lines[idx] = `| ${c.join(' | ')} |`;
  writeFileSync(PATHS.applications, lines.join('\n'), 'utf-8');
  return true;
}

function escTableCell(s) {
  return String(s || '')
    .replace(/\|/g, '/')
    .replace(/\r?\n/g, ' ')
    .trim();
}

function findDuplicatePipelineJob(url, company, title) {
  const jobs = parsePipeline();
  const u = url.trim();
  const sc = slug(company);
  const st = slug(title);
  return jobs.find((j) => j.url === u || (slug(j.company) === sc && slug(j.title) === st));
}

const APPLICATIONS_TEMPLATE = `# Applications Tracker

| # | Date | Company | Role | Score | Status | PDF | Report | Notes |
|---|------|---------|------|-------|--------|-----|--------|-------|
`;

function ensureApplicationsFile() {
  if (!existsSync(PATHS.applications)) {
    writeFileSync(PATHS.applications, APPLICATIONS_TEMPLATE, 'utf-8');
  }
}

function nextApplicationNumber() {
  const apps = parseApplications();
  let max = 0;
  for (const a of apps) {
    const n = parseInt(String(a.num).replace(/\D/g, ''), 10);
    if (!Number.isNaN(n)) max = Math.max(max, n);
  }
  return String(max + 1).padStart(3, '0');
}

/** Append one checkbox line under ## Pendientes (same strategy as scan.mjs). */
function insertPipelineLine(newLine) {
  const marker = '## Pendientes';
  let text = safeRead(PATHS.pipeline, '');
  if (!text.trim()) {
    text = `# Pipeline — job URL inbox\n\n${marker}\n\n`;
  }
  const idx = text.indexOf(marker);
  if (idx === -1) {
    const procIdx = text.indexOf('## Procesadas');
    const insertAt = procIdx === -1 ? text.length : procIdx;
    text = `${text.slice(0, insertAt)}\n${marker}\n\n${newLine}\n\n${text.slice(insertAt)}`;
  } else {
    const afterMarker = idx + marker.length;
    const nextSection = text.indexOf('\n## ', afterMarker);
    const insertAt = nextSection === -1 ? text.length : nextSection;
    text = `${text.slice(0, insertAt)}\n${newLine}\n${text.slice(insertAt)}`;
  }
  writeFileSync(PATHS.pipeline, text, 'utf-8');
}

/** Append a tracker row for dashboard-added jobs (canonical status only). */
function appendApplicationLine(company, title, dashboardStatus) {
  const canonical = dashboardStatusToCanonical(dashboardStatus);
  if (!canonical) return false;
  ensureApplicationsFile();
  const num = nextApplicationNumber();
  const date = new Date().toISOString().slice(0, 10);
  const co = escTableCell(company);
  const ro = escTableCell(title);
  const row = `| ${num} | ${date} | ${co} | ${ro} | —/5 | ${canonical} | ❌ | — | Added via dashboard |`;
  const text = safeRead(PATHS.applications, '');
  const out = text.replace(/\s*$/, '') + `\n${row}\n`;
  writeFileSync(PATHS.applications, out, 'utf-8');
  return true;
}

/** Match tracker row by company + role (same slugs as enrichJobs) and set canonical status. */
function writeApplicationStatusForJob(job, dashboardStatus) {
  if (!existsSync(PATHS.applications) || !job) return false;
  const canonical = dashboardStatusToCanonical(dashboardStatus);
  if (!canonical) return false;
  const text = safeRead(PATHS.applications, '');
  const lines = text.split(/\r?\n/);
  const apps = parseApplications();
  const appRow = apps.find((a) => slug(a.company) === slug(job.company) && slug(a.role) === slug(job.title));
  if (!appRow) return false;
  const idx = lines.findIndex((ln) => ln === appRow.line);
  if (idx < 0) return false;
  const c = appRow.line.split('|').map((x) => x.trim()).slice(1, -1);
  if (c.length < 9) return false;
  c[5] = canonical;
  lines[idx] = `| ${c.join(' | ')} |`;
  writeFileSync(PATHS.applications, lines.join('\n'), 'utf-8');
  return true;
}

function sendSse(res, event, payload) {
  res.write(`event: ${event}\n`);
  res.write(`data: ${JSON.stringify(payload)}\n\n`);
}

function broadcastUpdate(kind) {
  for (const client of updateClients) {
    sendSse(client, 'refresh', { kind, at: new Date().toISOString() });
  }
}

