function safeRead(path, fallback = '') {
  if (!existsSync(path)) return fallback;
  return readFileSync(path, 'utf-8');
}

function slug(v) {
  return String(v || '')
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, '-')
    .replace(/^-|-$/g, '');
}

function normalizeUrlForReportMatch(u) {
  return String(u || '')
    .trim()
    .replace(/\/+$/, '');
}

/**
 * When applications.md has no Report link, find evaluation markdown: filename slugs
 * (company + title), else newest report whose body contains the pipeline job URL.
 */
function resolveReportSnapshotForJob(job) {
  if (!existsSync(PATHS.reports) || !job?.url) return null;
  const names = readdirSync(PATHS.reports).filter((f) => f.toLowerCase().endsWith('.md'));
  if (names.length === 0) return null;

  const c = slug(job.company);
  const r = slug(job.title || '');

  function pickNewest(filteredNames) {
    if (filteredNames.length === 0) return null;
    let best = null;
    let bestT = -1;
    for (const name of filteredNames) {
      const p = join(PATHS.reports, name);
      const t = statSync(p).mtimeMs;
      if (t > bestT) {
        bestT = t;
        best = name;
      }
    }
    return best;
  }

  const both = r ? names.filter((n) => slug(n).includes(c) && slug(n).includes(r)) : [];
  let picked = pickNewest(both);

  if (!picked) {
    const co = names.filter((n) => slug(n).includes(c));
    picked = pickNewest(co);
  }

  const needle = normalizeUrlForReportMatch(job.url);
  if (!picked && needle) {
    const headBytes = 96 * 1024;
    let bestName = null;
    let bestT = -1;
    for (const name of names) {
      const p = join(PATHS.reports, name);
      let frag;
      try {
        const buf = readFileSync(p);
        frag = buf.length > headBytes ? buf.subarray(0, headBytes).toString('utf8') : buf.toString('utf8');
      } catch {
        continue;
      }
      if (!frag.includes(needle)) continue;
      const t = statSync(p).mtimeMs;
      if (t > bestT) {
        bestT = t;
        bestName = name;
      }
    }
    picked = bestName;
  }

  if (!picked) return null;
  try {
    const markdown = readFileSync(join(PATHS.reports, picked), 'utf-8');
    return { filename: picked, markdown };
  } catch {
    return null;
  }
}

/** Short excerpt from cv.md Professional Summary for report preview when no reports/*.md exists. */
function extractCvSummaryExcerpt(maxLen = 900) {
  const p = join(ROOT, 'cv.md');
  if (!existsSync(p)) return '';
  const cv = safeRead(p, '');
  const m = cv.match(/^##\s+Professional Summary\s*\n([\s\S]*?)(?=^##\s|\Z)/im);
  if (!m) return '';
  return m[1].trim().replace(/\s+/g, ' ').slice(0, maxLen);
}

