/**
 * Add a job URL to data/pipeline.md (and optionally data/applications.md).
 * Body: { url, company, title, status?, addToApplications? }
 * status: pending | evaluated | applied | interviewing | rejected | offer (default applied for "already applied")
 */
app.post('/api/jobs', (req, res) => {
  const url = String(req.body.url || '').trim();
  const company = escTableCell(req.body.company);
  const title = escTableCell(req.body.title);
  const status = String(req.body.status || 'applied').toLowerCase();
  const addToApplications = req.body.addToApplications !== false;

  try {
    void new URL(url);
  } catch {
    return res.status(400).json({ ok: false, error: 'Invalid URL (must start with http/https)' });
  }
  if (!company || !title) {
    return res.status(400).json({ ok: false, error: 'Company and job title are required' });
  }

  const dup = findDuplicatePipelineJob(url, company, title);
  if (dup) {
    return res.status(409).json({ ok: false, error: 'This URL or company+title is already in the pipeline', id: dup.id });
  }

  const mark = pipelineStatusToMark(status);
  const bracket = mark === ' ' ? '[ ]' : `[${mark}]`;
  const newLine = `- ${bracket} ${url} | ${company} | ${title}`;
  insertPipelineLine(newLine);

  let trackerRow = false;
  let trackerNote = null;
  if (addToApplications) {
    if (dashboardStatusToCanonical(status)) {
      trackerRow = appendApplicationLine(company, title, status);
    } else {
      trackerNote =
        'Tracker row was not created: pick a workflow status (e.g. applied, evaluated) or turn off “Add row to applications tracker”.';
    }
  }

  broadcastUpdate('pipeline');
  if (trackerRow) broadcastUpdate('applications');
  res.json({ ok: true, trackerRow, trackerNote });
});
